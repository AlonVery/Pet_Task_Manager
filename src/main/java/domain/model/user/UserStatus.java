package domain.model.user;

/**
 * ACTIVE — активный пользователь. Может входить в систему и выполнять действия.
 * <p>
 * INACTIVE — неактивный. Временно отключён (например, администратором).
 * <p>
 * PENDING — ожидает подтверждения. Например, не подтверждена почта или регистрация не завершена.
 * <p>
 * BLOCKED — заблокирован. Нарушение правил, подозрительная активность и т.п.
 * <p>
 * DELETED — удалён (soft delete). Пользователь логически удалён, но запись хранится в БД.
 * <p>
 * SUSPENDED — приостановлен. Временная блокировка на срок.
 */


public enum UserStatus {
    ACTIVE,
    INACTIVE,
    PENDING,
    BLOCKED,
    DELETED,
    SUSPENDED
}
